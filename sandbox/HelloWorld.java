// Arquivo: HelloWorld.java
public class HelloWorld {
    public static void main(String[] args) {
        // Exibe uma mensagem no console
        System.out.println("Hello, World!");

        // Declaração de variáveis básicas
        int idade = 20;                  // número inteiro
        double altura = 1.75;            // número decimal
        char inicial = 'E';              // caractere único
        String nome = "Edson";           // texto (string)
        boolean estudante = true;        // valor lógico

        // Exibe as variáveis no console
        System.out.println("Nome: " + nome);
        System.out.println("Idade: " + idade);
        System.out.println("Altura: " + altura + "m");
        System.out.println("Inicial: " + inicial);
        System.out.println("É estudante? " + estudante);

        String kau = "Kakau";

        System.out.println(kau);
    }
}

